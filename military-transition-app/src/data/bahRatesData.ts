/**
 * Look up BAH rate for a given location, grade, and dependency status.
 */
export function getBAHRate(
  locationId: string,
  grade: PayGrade,
  withDependents: boolean
): number {
  const location = bahLocations.find((l) => l.id === locationId)
  if (!location) return 0

  const rateTable = withDependents
    ? location.rates.withDependents
    : location.rates.withoutDependents

  // For O-8 through O-10, fall back to O-7 rates
  let lookupGrade = grade
  if (['O-8', 'O-9', 'O-10'].includes(grade)) {
    lookupGrade = 'O-7'
  }

  return rateTable[lookupGrade] || 0
}

/**
 * Get all location names sorted alphabetically.
 */
export function getLocationOptions(): Array<{ id: string; label: string }> {
  return bahLocations
    .map((l) => ({ id: l.id, label: `${l.name}, ${l.state}` }))
    .sort((a, b) => a.label.localeCompare(b.label))
}
