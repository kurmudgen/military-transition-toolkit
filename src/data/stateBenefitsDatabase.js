// Get all state abbreviations
export const getAllStates = () => {
  return Object.keys(STATE_BENEFITS_DATABASE).sort()
}

// Get state by abbreviation
export const getStateData = (abbreviation) => {
  return STATE_BENEFITS_DATABASE[abbreviation]
}

// Compare multiple states
export const compareStates = (stateAbbreviations) => {
  return stateAbbreviations.map(abbr => getStateData(abbr))
}

// Search states by criteria
export const searchStates = (criteria) => {
  return Object.values(STATE_BENEFITS_DATABASE).filter(state => {
    // Filter by benefit availability
    if (criteria.category && !state.benefits[criteria.category]?.available) {
      return false
    }

    // Filter by minimum rating
    if (criteria.minRating && state.rating < criteria.minRating) {
      return false
    }

    // Filter by name search
    if (criteria.search) {
      const searchLower = criteria.search.toLowerCase()
      return state.name.toLowerCase().includes(searchLower) ||
             state.abbreviation.toLowerCase().includes(searchLower)
    }

    return true
  })
}
