export function getStateData(stateCode) {
  return statesData[stateCode] || null;
}

export function compareStates(stateCodes) {
  return stateCodes.map(code => statesData[code]).filter(Boolean);
}

export function getStatesByCategory(category) {
  // category: 'noIncomeTax', 'fullPropertyTaxExemption', 'freeEducation'
  const categoryMap = {
    noIncomeTax: noIncomeTaxStates,
    fullPropertyTaxExemption: fullPropertyTaxExemptStates,
    freeEducation: freeEducationStates,
  };

  return (categoryMap[category] || []).map(code => statesData[code]).filter(Boolean);
}

export function calculateSavings(stateCode, profile) {
  const state = statesData[stateCode];
  if (!state) return null;

  const {
    disabilityRating = 0,
    militaryRetirement = 0,
    homeValue = 0,
    currentStateCode = '',
  } = profile;

  let savings = 0;

  // Property tax savings
  if (disabilityRating >= 100 && state.taxes.propertyTax100Pct >= 999999) {
    savings += homeValue * (state.taxes.propertyTaxRate / 100);
  } else if (disabilityRating >= 70) {
    const exemption = state.taxes.propertyTax70Pct;
    savings += exemption * (state.taxes.propertyTaxRate / 100);
  }

  // Income tax savings
  if (state.taxes.incomeTaxRate === 0 && militaryRetirement > 0) {
    // Compare to typical state with 5% income tax
    savings += militaryRetirement * 0.05;
  }

  return Math.round(savings);
}
